    private void selecionarRadio(int posicion){
        switch(posicion){
            case 0:
                radioRespuesta1.setSelected(true);
                break;
            case 1:
                radioRespuesta2.setSelected(true);
                break;
            case 2:
                radioRespuesta3.setSelected(true);
                break;
            case 3:
                radioRespuesta4.setSelected(true);
                break;
        }
    }
